# 字符串格式化
# ---> 方式1：使用占位符 %s 完成字符串和变量的快速拼接（% 代表我要占位，s代表将变量转为字符串放入占位的位置），注意，前面有多少个占位符（%s），后面就要有多少个变量（或数据），后面的变量用 %(变量名1,变量名2,......)来写
name = "阿韵"
age = 27
pro = "维修工"
hobby = "修屋顶"
print("大家好,我叫%s,今年%s岁,我的专业是%s,爱好是%s" % (name,age,pro,hobby))

# --->方式2：使用 f"内容{变量名/表达式}" 的形式来完成快速格式化---> 推荐方式
name = "阿韵"
age = 27
pro = "维修工"
hobby = "修屋顶"
print(f"大家好，我叫{name},今年{age}岁,我的专业是{pro},爱好是{hobby}")