# 字符串
# --->定义字符串的三种方式：'（单引号）、""（双引号）、"""（三引号），单引号、双引号是等效的，项目中保持一种写法就好，三引号用于多行字符串等场景
s1 = "hello" #双引号定义
s2 = 'hello' #单引号定义
s3 = """  
Hello:
    我现在在学习python
    我要努力学习，找到好工作
    加油！
""" # 三引号定义（多行字符串）
print(s1,s2,s3)

# --->转义字符： \' （单引号） \" （双引号）  \n （换行）  \t （制表符）
msg = 'It\'s very good.' #使用单引号定义后，单引号里面不可再出现单引号，如果需要，则加上转义字符 \'
print(msg)

msg2 = "It's very good." #又或者使用双引号来定义字符串，则可以避免单引号冲突
print(msg2)

msg3 = "hello 的意思是\"你好\"" #用双引号定义时，字符串中也有双引号，则字符串里的双引号可加一个 \" 的转义字符来避免冲突
print(msg3)

msg4 = 'hello 的意思是"你好"' #当字符串中有双引号，则可用单引号来定义，避免冲突
print(msg4)

print("曾韵是一个大美女！\n赞同的请举手！") # 如果想要换行，可加一个 \n 的转义字符
print("\t曾韵是一个大美女！\n\t赞同的请举手！") # \n 换行 、\t 按了tab缩进

# ---> 字符串拼接
s1 = "人生苦短""我用python"",ok" #多个字符串字面量直接写（其实这种写法了解一下就行，因为可以直接写成一个字符串）
