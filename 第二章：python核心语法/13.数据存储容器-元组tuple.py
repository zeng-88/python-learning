#     print(f"{i[0]} \t {i[1]} \t {i[2]} \t {i[3]} \t {i[4]} \t {total} \t {avg:.1f}")
"""
上面使用索引的方法，代码可读性比较弱，如果不熟悉元组的结构，写到后面会不知道自己写的是什么（0，1，2，3这些数字代表什么呢？
那么可以通过元组解包的方式，给元组内的值分别赋值给对应的变量，可以更直观，比如id="序号"、name="姓名"等，不用看原来的元组，也知道是什么。
👇👇👇👇
"""
# 方法2：元组解包
for id,name,chinese,math,english in students:
    total = chinese + math + english
    avg = total / 3
    print(f"{id} \t {name} \t {chinese} \t {math} \t {english} \t {total} \t {avg:.1f}")
print("-------------------------------------------------------")

# 2.统计各科成绩的最低分、最高分、平均分，并输出。
# 2.1获取各科成绩的列表
chinese_scores = [i[2]for i in students]  # 使用了列表推导式，形成一个各科的成绩列表
math_scores = [i[3]for i in students]
english_scores = [i[4]for i in students]
# 2.2统计各科成绩的最低分、最高分、平均分
print("学科 \t 最低分 \t 最高分 \t 平均分")  #表头
print(f"语文 \t {min(chinese_scores)} \t {max(chinese_scores)} \t {sum(chinese_scores)/len(chinese_scores)}")
print(f"数学 \t {min(math_scores)} \t {max(math_scores)} \t {sum(math_scores)/len(math_scores)}")
print(f"英语 \t {min(english_scores)} \t {max(english_scores)} \t {sum(english_scores)/len(english_scores)}")
print("-------------------------------------------------------")

