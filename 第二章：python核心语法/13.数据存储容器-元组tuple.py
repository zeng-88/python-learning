"""
解题思路：
存储学生的信息，每一行为一个信息，可以用一个元组来表示，全部信息也可以用元组表示，那就可以进行元组的嵌套。为什么用元组？因为学生的成绩一旦录入，不可修改。
"""
students = (
    ("S001","王琳",85,92,78),
    ("S002","李小龙",92,88,95),
    ("S003","王琳",85,92,78),
    ("S004","吴嘉莉",95,89,98),
    ("S005","曾韵",96,90,99),
    ("S006","曾轶可",80,86,87),
    ("S007","麻园",88,96,87),
    ("S008","甜甜",80,99,90),
    ("S009","五百",85,97,96),
    ("S010","许大脚",95,95,91)
)

# 1.计算每个学生的总分、各科平均分，然后一并输出出来。---> avg:.1f ---> 这里是对平均分保留1位小数处理，冒号代表对前面的元素进行解释，.1表示保留1位小数，f是avg的数据类型float
print("学号 \t 姓名 \t 语文 \t 数学 \t 英语 \t 总分 \t 平均分")  #表头
# 方法1：索引
# for i in students:
#     total = i[2]+i[3]+i[4]
