# 当存储大量数据时，如果通过定义一个一个变量去存储，这会非常繁琐，因此引入数据容器，只需定义一个变量，就能存储大量数据
# 数据容器：是指一种可以容纳多份数据的数据类型（容器），容纳的每一份数据称之为1个元素，每一个元素都可以是任意类型的数据，如：字符串、数字、bool等。
# 数据容器的类型：列表（list）、字符串（str）、元组（tuple）、集合（set）、字典（dict）


# 1、列表（list）
# 定义：列表名称 = [元素1,元素2,元素3,元素4...]
# 特点：1、可以存储不同类型的元素
#      2、元素有序、可以重复、元素可以修改
