#---------------- 列表 list 切片---------------------
# 语法：序列数据[开始索引:结束索引：步长]
#     开始索引默认从0开始，结束索引不包含其位置对应的元素
#     采用正向、反向索引都可以
#     步长是选取间隔，默认步长为1

# 举例：
# 定义列表
s = ["A","B","C","D","E","F"]

# 切片操作 s[开始索引:结束索引:步长]
print(s[0:4:1])  # 截取第1至第4个元素，从第1个元素开始索引，第5个元素结束索引，步长为1

# 更简洁的写法：如果是从第一个元素开始截取，并且步长为1，可以省略开始索引和步长以及第二个冒号,效果相同
print(s[:4])

print(s[1:4:2])  # 从第2个元素开始截取，到第4个元素结束，每间隔2个元素截取1次
print(s[1:-2:1]) # 反向索引：从第1个元素开始截取，到倒数第二个元素结束